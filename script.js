// ================= FUNCTION =================
function changeContent(section, btn) {

    const box = document.getElementById("content");

    // 🔥 Convert text into multiple paragraphs
    let formattedText = data[section].text
        .split("\n\n")
        .map(p => `<p>${p}</p>`)
        .join("");

    box.innerHTML = `
        <h2 class="content-title">${data[section].title}</h2>
        ${formattedText}
    `;

    // 🔥 Active button highlight
    document.querySelectorAll(".navbar button").forEach(b => b.classList.remove("active-btn"));
    if (btn) btn.classList.add("active-btn");
}

// ================= LOAD DEFAULT =================
window.onload = function () {
    const firstBtn = document.querySelector(".navbar button");
    changeContent("home", firstBtn);
};
